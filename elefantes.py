def cancion_elefantes():
    print("1 elefante se balanceaba sobre la tela de una arana, como veia que resistia, fue a llamar a otro elefante.")
    contador = 2
    while contador <= 10:
        print(f"{contador} elefantes se balanceaban sobre la tela de una arana, como veian que resistia, fueron a llamar a otro elefante.")
        contador += 1
    
## Fin
cancion_elefantes()